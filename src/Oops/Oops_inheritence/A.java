package Oops.Oops_inheritence;

public class A {

    public void m1(){
        System.out.println("this is m1 method of parent class A");
    }
    public void m2(){
        System.out.println("this is m2 method of parent class A");
    }
    public void m3(){
        System.out.println("this is m3 method of parent class A");

    }
    public void m4(){
        System.out.println("this is m4 method of parent class A");

    }

    public static void main(String[] args) {
        A obj=new A();
        obj.m1();
        obj.m2();
        obj.m3();
        obj.m4();
    }
    }

